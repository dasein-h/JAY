package com.example.classno3_demo.music;
public class Music {
    private String name;
    private String word;
    private int photo;

    public  void  setName(String name) {
        this.name = name;
    }

    public String getName() {
        return  name;
    }

    public  void  setWord(String word) {
        this.word = word;
    }

    public String getWord() {
        return  word;
    }

    public  void  setPhoto(int photo) {
        this.photo = photo;
    }

    public int getPhoto() {
        return  photo;
    }
}
